public class jex1 {
    public static void main(String[] args)
    {
        int a=5;
        int b=9;
        System.out.println(a&b);
        System.out.println(a|b);
        System.out.println(a^b);//bitwise ex-or => if both bits match then 0 else 1
        System.out.println(~a);// bitwise not
    }
}
